package weekOne.day3;

public class FindingEvenNumber {
	
	public static void main(String[] args) {
		int a=10;
		if (a%2==0) {
			System.out.println("The value is Even number "+ a);
		}
		else if (a%2==1) {
			System.out.println("The value is odd number "+ a);
		}
		else {
			System.out.println("Not a valid number");
		}
	}

}
